package uk.ac.nottingham.ningboport.client.test;

/**
 * An Object to save all the Gps
 * @author Haoyue Zhu
 *
 */
public class GPSManager {
	private double longitude;
	private double latitude;
	private int sessionID;
	private String driverName;
	private String vehicleID;
	private long startTime;
	private long expireTime;
	private long time;
	
	public GPSManager(int sessionID, double longitude, double latitude, String driverName, String vehicleID, 
			long startTime, long expireTime, long time){
		this.sessionID = sessionID;
		this.longitude = longitude;
		this.latitude = latitude;
		this.driverName = driverName;
		this.vehicleID = vehicleID;
		this.startTime = startTime;
		this.expireTime = expireTime;
		this.time = time;
	}
	
	public int getSessionID() {
		return sessionID;
	}
	
	public void setSessionID(int sessionID) {
		this.sessionID = sessionID;
	}
	
	public long getTime() {
		return time;
	}

	public void setTime(long time) {
		this.time = time;
	}

	public double getLongitude() {
		return longitude;
	}

	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}

	public double getLatitude() {
		return latitude;
	}

	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}

	public String getDriverName() {
		return driverName;
	}

	public void setDriverName(String driverName) {
		this.driverName = driverName;
	}

	public String getVehicleID() {
		return vehicleID;
	}

	public void setVehicleID(String vehicleID) {
		this.vehicleID = vehicleID;
	}

	public long getStartTime() {
		return startTime;
	}

	public void setStartTime(long startTime) {
		this.startTime = startTime;
	}

	public long getExpireTime() {
		return expireTime;
	}

	public void setExpireTime(long expireTime) {
		this.expireTime = expireTime;
	}

	
}
